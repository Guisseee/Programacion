/*Programa que Muestra los números del 320 al 160, contando de 20 en 20 hacia atrás utilizando
un bucle while
 * 
 * Autor:Guillermo Jáuregui Lahoz
 * 
 */
public class Ejercicio5{
  public static void main (String[] args){
	
	int cont=320;
	
	while(cont>=160){
		System.out.println(cont);
		cont-=20;
	}
}
}
